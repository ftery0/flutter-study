// ============================================================
// Dart 기초 2: 함수
// ============================================================

void main() {
  // ----------------------------------------------------------
  // 1. 기본 함수
  // ----------------------------------------------------------

  greet('홍길동');
  greet('김철수');

  // ----------------------------------------------------------
  // 2. 반환값이 있는 함수
  // ----------------------------------------------------------

  int result = add(3, 5);
  print('\n3 + 5 = $result');

  double avg = average(10, 20, 30);
  print('평균: $avg');

  // ----------------------------------------------------------
  // 3. 선택적 매개변수 - 넘겨도 되고 안 넘겨도 되는 파라미터
  // ----------------------------------------------------------

  // [] 로 감싸면 선택적 위치 매개변수
  introduce('홍길동');           // 나이 없이 호출
  introduce('김철수', 25);       // 나이 포함 호출

  // ----------------------------------------------------------
  // 4. 이름있는 매개변수 - Flutter에서 가장 많이 쓰는 방식!
  // ----------------------------------------------------------

  // {} 로 감싸면 이름있는 매개변수
  // 호출할 때 파라미터 이름을 직접 써줘요
  printProfile(name: '홍길동', age: 25, city: '서울');
  printProfile(name: '김철수', city: '부산', age: 30);  // 순서 달라도 OK

  // ----------------------------------------------------------
  // 5. 화살표 함수 - 한 줄짜리 함수를 간결하게
  // ----------------------------------------------------------

  int doubled = double2(7);
  print('\n7 * 2 = $doubled');

  // ----------------------------------------------------------
  // 6. 함수를 변수에 담기
  // ----------------------------------------------------------

  // 함수도 변수처럼 담을 수 있어요
  var myFunc = add;
  print('함수 변수로 호출: ${myFunc(10, 20)}');

  // ----------------------------------------------------------
  // 7. 익명 함수 (람다)
  // ----------------------------------------------------------

  var square = (int n) => n * n;
  print('5의 제곱: ${square(5)}');

  // 리스트와 함께 자주 써요
  List<int> numbers = [1, 2, 3, 4, 5];
  List<int> squared = numbers.map((n) => n * n).toList();
  print('제곱 리스트: $squared');
}

// ----------------------------------------------------------
// 함수 정의들
// ----------------------------------------------------------

// void: 반환값 없음
void greet(String name) {
  print('안녕하세요, $name 님!');
}

// int를 반환하는 함수
int add(int a, int b) {
  return a + b;
}

// double을 반환하는 함수
double average(int a, int b, int c) {
  return (a + b + c) / 3;
}

// 선택적 위치 매개변수 - age는 없어도 됨
void introduce(String name, [int? age]) {
  if (age != null) {
    print('\n저는 $name 이고 $age 살입니다.');
  } else {
    print('\n저는 $name 입니다.');
  }
}

// 이름있는 매개변수 - required 붙이면 필수
void printProfile({
  required String name,
  required int age,
  required String city,
}) {
  print('\n[프로필] 이름: $name | 나이: $age | 도시: $city');
}

// 화살표 함수 (=> 로 한줄 표현)
int double2(int n) => n * 2;
