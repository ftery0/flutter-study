// ============================================================
// Dart 기초 1: 변수와 타입
// ============================================================

void main() {
  // ----------------------------------------------------------
  // 1. 기본 타입
  // ----------------------------------------------------------

  // int: 정수
  int age = 25;

  // double: 소수점 있는 숫자
  double height = 175.5;

  // String: 문자열 (큰따옴표, 작은따옴표 둘 다 OK)
  String name = '홍길동';

  // bool: true / false
  bool isStudent = true;

  print('이름: $name');      // $ 로 변수를 문자열 안에 넣을 수 있어요
  print('나이: $age');
  print('키: $height');
  print('학생: $isStudent');

  // ----------------------------------------------------------
  // 2. var - 타입을 자동으로 추론
  // ----------------------------------------------------------

  var city = '서울';      // Dart가 String으로 자동 인식
  var score = 98;         // int로 자동 인식

  print('\n도시: $city');
  print('점수: $score');

  // ----------------------------------------------------------
  // 3. final vs const - 값을 바꿀 수 없는 변수
  // ----------------------------------------------------------

  // final: 런타임에 값이 정해짐 (한번 정해지면 변경 불가)
  final String country = '대한민국';

  // const: 컴파일 타임에 값이 정해짐 (더 엄격한 상수)
  const double PI = 3.14159;

  print('\n나라: $country');
  print('파이: $PI');

  // country = '미국';  // 오류! final은 변경 불가
  // PI = 3.0;          // 오류! const도 변경 불가

  // ----------------------------------------------------------
  // 4. null 처리 - Dart는 기본적으로 null을 허용하지 않아요
  // ----------------------------------------------------------

  // String nickname = null;  // 오류! null 불가

  // null을 허용하려면 타입 뒤에 ? 를 붙여요
  String? nickname = null;   // OK
  print('\n닉네임: $nickname');

  nickname = '길동이';
  print('닉네임 변경: $nickname');

  // ----------------------------------------------------------
  // 5. 문자열 사용법
  // ----------------------------------------------------------

  String firstName = '길동';
  String lastName = '홍';

  // 문자열 합치기
  String fullName = lastName + firstName;
  print('\n풀네임: $fullName');

  // 여러 줄 문자열
  String message = '''
안녕하세요,
저는 $fullName 입니다.
나이는 $age 살이에요.
  ''';
  print(message);
}
